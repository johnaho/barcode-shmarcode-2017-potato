	br getkeys

	;; Function:	clrscr
	;;
	;; Clears the screen

clrscr:	
	clr1 ocr,5
	push acc
	push xbnk
	push 2
	mov #0,xbnk
.cbank:	mov #$80,2
.cloop:	mov #0,@R2
	inc 2
	ld 2
	and #$f
	bne #$c,.cskip
	ld 2
	add #4
	st 2
.cskip:	ld 2
	bnz .cloop
	bp xbnk,0,.cexit
	mov #1,xbnk
	br .cbank
.cexit:	pop 2
	pop xbnk
	pop acc
	set1 ocr,5
	ret	

	;; Function:    invertRows
	;;
	;; Inverts specified rows on the screen
invertRows:
	clr1 ocr,5
	push acc
	mov #42, bytesRemaining
.iloop:	ld @R2
	xor #$ff
	st @R2
	dec bytesRemaining
	ld bytesRemaining
	bz .iexit
	inc 2
	ld 2
	and #$f
	bne #$c,.iskip
	ld 2
	add #4
	st 2
.iskip:	ld 2
	bnz .iloop
	bp xbnk,0,.iexit
	mov #1,xbnk
	mov #$80, 2
	br .iloop
.iexit:
	pop acc
	set1 ocr,5
	ret	

	;; Function:	setscr
	;;
	;; Copies a predefined full-screen image to the screen
	;;
	;; Inputs:
	;;   trl = low byte of predefined screen ROM address
	;;   trh = high byte of predefined screen ROM address
	
setscr:
	clr1 ocr,5
	push acc
	push xbnk
	push c
	push 2
	mov #$80,2
	xor acc
	st xbnk
	st c
.sloop:	ldc
	st @R2
	inc 2
	ld 2
	and #$f
	bne #$c,.sskip
	ld 2
	add #4
	st 2
	bnz .sskip
	inc xbnk
	mov #$80,2
.sskip:	inc c
	ld c
	bne #$c0,.sloop
	pop 2
	pop c
	pop xbnk
	pop acc
	set1 ocr,5
	ret

