getkeys:
	bp p7,0,quit ; Quit if plugged into controller 
	ld p3 ; Load keys
	bn acc,6,quit
	bn acc,7,sleep
	ret

quit:
	jmp goodbye
	
sleep:
	bn p3,7,sleep	; Wait for SLEEP to be depressed
	mov #0,vccr	; Blank LCD
sleepmore:
	set1 pcon,0	; Enter HALT mode
	bp p7,0,quit	; Docked?
	bp p3,7,sleepmore	; No SLEEP press yet
	mov #$80,vccr	; Reenable LCD
