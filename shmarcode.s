start:
	clr1 ie,7
	mov #$a1,ocr
	mov #$09,mcr
	mov #$80,vccr
	clr1 p3int,0
	clr1 p1,7
	mov #$ff,p3
	
	set1 ie,7

	brf win
	; Get current time
	clr1 psw, 1
	ld $1b ; Hours
	;bne #12, showMainScreen
	ld $1c ; Minutes
	bnz tooLate
	ld $1d ; Seconds
	sub #2
	and #$80
	bz tooLate
	set1 psw, 1
	br startGame

showMainScreen:
	set1 psw, 1
	call clrscr

